        <div class="guidelines-section">
            <h3>General Rules</h3>
            <ul class="guidelines-list">
                <li>Be respectful and constructive in all interactions.</li>
                <li>Search before posting to avoid duplicate questions or requests.</li>
                <li>Keep discussions relevant to the Argo Books project.</li>
                <li>Use clear titles and provide context and details in your posts.</li>
                <li>No spam, advertising or self-promotion.</li>
                <li>Use votes to highlight helpful posts and comments.</li>
                <li>Use comments for clarification and keep them civil.</li>
                <li>Include useful information when reporting bugs or requesting features.</li>
                <li>Moderators may edit or remove posts that don't follow these guidelines.</li>
            </ul>
        </div>

        <!-- Professional Conduct -->
        <div class="guidelines-section">
            <h3>Professional Conduct</h3>
            <ul class="guidelines-list">
                <li>Maintain professional language appropriate for a business software community.</li>
                <li>Avoid personal attacks, harassment, or discriminatory language.</li>
                <li>Respect differing experience levels, and business needs.</li>
                <li>Stay patient when helping newcomers or explaining concepts.</li>
                <li>Focus on the software issue, not personal characteristics of other users.</li>
            </ul>
        </div>

        <!-- Prohibited Content -->
        <div class="guidelines-section">
            <h3>Prohibited Content</h3>
            <div class="important-note">
                <strong>The following types of content are not permitted and will result in immediate removal and
                    potential account suspension:</strong>
            </div>
            <ul class="guidelines-list">
                <li>Sexual, explicit, or inappropriate content of any kind.</li>
                <li>Hate speech, harassment, threats, or discriminatory content.</li>
                <li>Illegal activities or content that violates laws.</li>
                <li>Malicious software, viruses, or security exploits.</li>
                <li>Doxxing or sharing personal information without consent.</li>
                <li>Impersonation of other users, companies, or Argo team members.</li>
                <li>Copyright infringement or unauthorized sharing of proprietary content.</li>
                <li>Excessive profanity or inappropriate language.</li>
                <li>Off-topic discussions unrelated to Argo Books.</li>
            </ul>
        </div>

        <!-- Privacy & Security -->
        <div class="guidelines-section">
            <h3>Privacy & Security Guidelines</h3>
            <ul class="guidelines-list">
                <li>Never share your license keys, passwords, or account credentials publicly. Admins will never ask you
                    for your key or personal information!</li>
                <li>Avoid sharing detailed customer information or financial data.</li>
                <li>Report security vulnerabilities privately to <a
                        href="mailto:support@argorobots.com">support@argorobots.com</a>.</li>
                <li>Use discretion when sharing business processes or proprietary workflows.</li>
                <li>Respect the privacy of other community members.</li>
            </ul>
        </div>

        <!-- Account Guidelines -->
        <div class="guidelines-section">
            <h3>Account Guidelines</h3>
            <ul class="guidelines-list">
                <li>One account per person - multiple accounts are not permitted.</li>
                <li>Use a recognizable username, preferably related to your business or real name.</li>
                <li>Complete email verification to ensure account security.</li>
                <li>Keep your contact information current for important updates.</li>
                <li>Don't share accounts or let others post using your credentials.</li>
            </ul>
        </div>

        <!-- Enforcement -->
        <div class="guidelines-section">
            <h3>Enforcement & Consequences</h3>
            <ul class="guidelines-list">
                <li><strong>First violation:</strong> Warning and content removal.</li>
                <li><strong>Repeated violations:</strong> Temporary account suspension (1-7 days).</li>
                <li><strong>Serious violations:</strong> Permanent account ban and content removal.</li>
                <li>Appeals can be submitted to <a href="mailto:support@argorobots.com">support@argorobots.com</a>.</li>
                <li>Moderators have final discretion in enforcement decisions.</li>
            </ul>
